package main

import "fmt"

func main() {
	var variavel1 string = "variavel 1"
	fmt.Println(variavel1)
	variavel2 := "variavel 2"
	fmt.Println(variavel2)
	var numero int = 100000
	fmt.Println(numero)
	var numero2 float64 = 12.34
	fmt.Println(numero2)

	var boleano1 bool = true
	fmt.Println(boleano1)

	var erro error
	fmt.Println(erro)
}
